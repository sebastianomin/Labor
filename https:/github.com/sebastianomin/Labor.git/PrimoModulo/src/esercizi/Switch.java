package esercizi;

public class Switch {
	public static void main(String[] args) {
		
		int c=9;
		
		switch (c) {
		case 1:
			System.out.println("1");
			
		case 2:
			System.out.println("2");
			break;
		case 3:
			System.out.println("3");
			
		case 4:
			System.out.println("4");
			
		default :
			System.out.println("4+");
			
		
		}
		
		
		
	}

}
